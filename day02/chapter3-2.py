# if ~ else
number = int( input("정수 입력 > ") )
if number % 2 == 0:
    print("짝수")
else :
    print("홀수")

# if ~ elif ~ else
if number % 2 == 0 :
    print("짝수")
elif number % 2 == 1 :
    print("홀수")
else :
    print("잘못된 값")

